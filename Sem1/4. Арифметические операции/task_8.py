# Сложение
print(398 + 20)  # -> 418

# Вычитание
print(200 - 50)  # -> 150

# Умножение
print(34 * 7)  # -> 238

# Деление
print(36 / 6)  # -> 6.0
print(36 / 5)  # -> 7.2
print(round(36 / 7, 2))  # -> 5.14
print(round(-36 / -7, 3))  # -> 5.143

# Целочисленное деление
print(36 // 6)  # -> 6
print(36 // 5)  # -> 7
print(-9 // 4)  # -> -3
print(5 // -2)  # -> -3

# Остаток от деления
print(36 % 6)  # -> 0
print(36 % 5)  # -> 1

# Возведение в степень
print(2 ** 16)  # -> 65536
