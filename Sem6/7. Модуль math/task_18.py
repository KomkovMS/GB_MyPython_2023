from math import ceil, fabs, factorial, floor, \
   fmod, isfinite, modf, sqrt, sin, cos, tan, degrees, radians

print(f"ceil() -> {ceil(6.75)}")  # ceil() -> 7
print(f"fabs() -> {fabs(-4)}")  # fabs() -> 4.0
print(f"factorial() -> {factorial(5)}")  # factorial() -> 120
print(f"floor() -> {floor(4.34)}")  # floor() -> 4
print(f"fmod() -> {fmod(9, 4)}")  # fmod() -> 1.0
print(f"isfinite() -> {isfinite(10)}")  # isfinite() -> True
print(f"modf() -> {modf(10.5)}")  # modf() -> (0.5, 10.0)
print(f"sqrt() -> {sqrt(16)}")  # sqrt() -> 4.0
print(f"sin() -> {sin(1.5708)}")  # sin() -> 0.9999999999932537
print(f"cos() -> {cos(1.5708)}")  # cos() -> -3.673205103346574e-06
print(f"tan() -> {tan(1.5708)}")  # tan() -> -272241.80840927624
print(f"degrees() -> {degrees(1.5708)}")  # degrees() -> 90.00021045914971
print(f"radians() -> {radians(90)}")  # radians() -> 1.5707963267948966
